    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
     DefaultTableModel model = ( DefaultTableModel ) stuinfotab.getModel();
     
     if(stuinfotab.getSelectedRowCount()==1)
     {
        JFrame frame = new JFrame("Delete");
        if (JOptionPane.showConfirmDialog(frame,"Confirm if you want to Delete this Record ", "Student Information Manager" , JOptionPane.YES_NO_OPTION)== JOptionPane.YES_NO_OPTION)
        {
         try{
            String genroll=tenroll.getText();
            Class.forName("java.sql.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/stu","anurag","anu404");
            stmt = con.createStatement();
            String query = "DELETE FROM stuinfo WHERE enroll='"+genroll+"';";
            stmt.executeUpdate(query);
            JOptionPane.showMessageDialog(null,"Record Deleted");
            
            
            model.removeRow(stuinfotab.getSelectedRow());
         }

         catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error in Deletion");
    }

        }
     }
     else if(stuinfotab.getRowCount()==0)
     {
         JOptionPane.showMessageDialog(this, "Table is Empty ");
     }
     else
     {
         JOptionPane.showMessageDialog(this, "Please Select a Row to Delete it ");  
     }
         
     
    }//GEN-LAST:event_deletebtnActionPerformed

