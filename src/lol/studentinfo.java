    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed
    DefaultTableModel model = ( DefaultTableModel ) stuinfotab.getModel();
    while(model.getRowCount() > 0){
        model.removeRow(0);
        }
    try{
        
        Class.forName("java.sql.Driver");
        con = DriverManager.getConnection("jdbc:mysql://localhost/stu","anurag","anu404");
        stmt = con.createStatement();
        String q = "Select * from stuinfo;";
        rs = stmt.executeQuery(q);
        while(rs.next()){
            String enroll= rs.getString("enroll");
            String first= rs.getString("first");
            String last= rs.getString("last");
            String sem= rs.getString("sem");
            String phone= rs.getString("phone");
            String gender= rs.getString("gender");
            String dob= rs.getString("dob");
            String email= rs.getString("email");
            
            
            model.addRow( new Object[] {enroll,first,last,sem,phone,gender,dob,email});
            }
    }
    catch(Exception e){
        JOptionPane.showMessageDialog(null,"Error in Connectivity");
    }
    }//GEN-LAST:event_updatebtnActionPerformed

